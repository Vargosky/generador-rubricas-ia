export const reglasMatematicas = `
Reglas para planificar en la asignatura de Matemáticas:

1. Las clases deben desarrollarse progresivamente desde lo concreto a lo abstracto, con foco en la resolución de problemas.
2. Se deben alternar actividades de razonamiento lógico, representación gráfica, modelamiento y cálculo.
3. Incluir actividades prácticas que relacionen las matemáticas con contextos reales y significativos para el estudiante.
4. Promueve el uso del lenguaje matemático formal, la argumentación y la justificación de procedimientos.
5. Cada clase debe incluir instancias de ejercitación guiada, trabajo autónomo y retroalimentación formativa.
6. Las evaluaciones deben considerar la comprensión de conceptos, aplicación de procedimientos y capacidad para resolver problemas.
7. Fomenta el uso de materiales manipulativos, tecnología (calculadora, GeoGebra, hojas de cálculo) y trabajo colaborativo.
8. Los objetivos deben reflejar habilidades como estimar, comparar, representar, aplicar fórmulas y generalizar patrones.
9. Se debe utilizar un lenguaje matemático claro, coherente y preciso, sin adornos innecesarios.
10. El cierre debe incluir sistematización, corrección colectiva o reflexión sobre los errores y aprendizajes.
`;
